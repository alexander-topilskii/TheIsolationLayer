const tables: Record<Locale, StringTable> = { ru, en };

export function uiString(
  locale: Locale,
  key: UiKey,
  params?: Record<string, string | number>,
): string {
  let text = tables[locale][key] ?? tables.ru[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.split(`{${k}}`).join(String(v));
    }
  }
  return text;
}
